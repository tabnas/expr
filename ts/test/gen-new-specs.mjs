import { writeFileSync } from 'fs'
const { omap } = Jsonic.util

const C = (x) => JSON.parse(JSON.stringify(x))
const S = (x, seen) => (
  seen = seen ?? new WeakSet(),
  seen?.has(x) ? '[CIRCLE]' : (
    (x && 'object' === typeof x ? seen?.add(x) : null),
    (x && Array.isArray(x)) ?
      (0 === x.length ? x : [
        x[0].src || S(x[0], seen),
        ...(1 < x.length ? (x.slice(1).map((t) => S(t, seen))) : [])]
        .filter(t => undefined !== t)) :
      (null != x && 'object' === typeof (x) ? omap(x, ([n, v]) => [n, S(v, seen)]) : x)))
const mj = (je) => (s) => C(S(je(s)))

function gen(name, inputs, j) {
  const lines = [`# ${name}\n# input\texpected_output`]
  for (const input of inputs) {
    try {
      const result = j(input)
      lines.push(`${input}\t${JSON.stringify(result)}`)
    } catch (e) {
      console.error(`ERROR: ${name}: ${input}: ${e.message}`)
    }
  }
  writeFileSync(`test/spec/${name}.tsv`, lines.join('\n') + '\n')
  console.log(`Wrote ${name}.tsv (${inputs.length} cases)`)
}

